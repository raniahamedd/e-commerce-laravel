    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {

        $product = Product::findOrFail($id);

        $tags = implode(',', $product->tags()->pluck('name')->toArray());

        return view('dashboard.products.edit', compact('product', 'tags'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Product $product)
    {
        $product->update($request->except('tags'));
        // مكتبة التاقينفاي بترجع صيغة جيسون فانا بدي احولها لاري عشان اتعالمل معها
        $tags = json_decode($request->tags); //هيك بتتحول لاري اوف اوبجيكت
        $tags_id = [];
        $saved_tags = Tag::all();
        foreach ($tags as $item) { //object عبارة عن  item هيك بتصير ال
            $slug = Str::slug($item->value);
            $tag = $saved_tags->where('slug', $slug)->first();
            if (!$tag) {
                $tag = Tag::create([
                    'name' => $item->value,
                    'slug' => $slug
                ]);
            }
            $tags_id[] = $tag->id;
        }

        $product->tags()->sync($tags_id);

        return redirect()->route('dashboard.products.index')
            ->with('success', 'updated successfaully');
    }

